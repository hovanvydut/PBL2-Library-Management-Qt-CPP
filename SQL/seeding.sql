CREATE DATABASE LibraryManagement
GO

USE LibraryManagement

CREATE TABLE authors(
	author_id INT NOT NULL IDENTITY(1,1),
	name VARCHAR(255),
	created_at DATE,
	updated_at DATE DEFAULT GETDATE(),
	deleted_at DATE,
	PRIMARY KEY(author_id)
)

CREATE TABLE publishers(
	publisher_id INT NOT NULL IDENTITY(1,1),
	name VARCHAR(255),
	created_at DATE,
	updated_at DATE DEFAULT GETDATE(),
	deleted_at DATE,
	PRIMARY KEY(publisher_id)
)

CREATE TABLE issuing_company(
	issuing_company_id INT NOT NULL IDENTITY(1,1),
	name VARCHAR(255),
	created_at DATE,
	updated_at DATE DEFAULT GETDATE(),
	deleted_at DATE,
	PRIMARY KEY(issuing_company_id)
)

CREATE TABLE categories(
	categories_id INT NOT NULL IDENTITY(1,1),
	name VARCHAR(255),
	created_at DATE,
	updated_at DATE DEFAULT GETDATE(),
	deleted_at DATE,
	PRIMARY KEY(categories_id),
)

CREATE TABLE books (
	book_id INT NOT NULL IDENTITY(1,1),
	title VARCHAR(255),
	cover_type VARCHAR(255),
	price FLOAT,
	total INT CHECK(total >= 0),
	available INT CHECK(available >= 0),
	publication_date DATE,
	size VARCHAR(255),
	number_of_pages INT CHECK(number_of_pages >= 0),
	issuing_company_id INT,
	publisher_id INT,
	category_id INT,
	created_at DATE,
	updated_at DATE DEFAULT GETDATE(),
	deleted_at DATE
	PRIMARY KEY(book_id)
)
ALTER TABLE dbo.books ADD CONSTRAINT FK_books_issuing_company FOREIGN KEY(issuing_company_id) REFERENCES dbo.issuing_company(issuing_company_id)
ALTER TABLE dbo.books ADD CONSTRAINT FK_books_publishers FOREIGN KEY (publisher_id) REFERENCES dbo.publishers(publisher_id)
ALTER TABLE dbo.books ADD CONSTRAINT FK_books_cateogries FOREIGN KEY (category_id) REFERENCES dbo.categories(categories_id)

CREATE TABLE author_books(
	author_id INT,
	book_id int
)
ALTER TABLE dbo.author_books ALTER COLUMN author_id INT NOT NULL
ALTER TABLE dbo.author_books ALTER COLUMN book_id INT NOT NULL 
ALTER TABLE dbo.author_books ADD CONSTRAINT PK_author_books PRIMARY KEY (author_id, book_id)
ALTER TABLE dbo.author_books ADD CONSTRAINT FK_author_books_authors FOREIGN KEY(author_id) REFERENCES dbo.authors(author_id)
ALTER TABLE dbo.author_books ADD CONSTRAINT FK_author_books_books FOREIGN KEY (book_id) REFERENCES dbo.books(book_id)

